/**
 * op_add     - Returns the sum of @a and @b
 *
 * @a:          To be added to @b
 * @b:          To be added to @a
 *
 * Return:      The sum of @a and @b
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub     - Returns the difference of @a and @b
 *
 * @a:          Used to subtract @b
 * @b:          To be subtracted from @a
 *
 * Return:      The difference of @a and @b
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul     - Returns the product of @a and @b
 *
 * @a:          To be multiplied to @b
 * @b:          To be multiplied to @a
 *
 * Return:      The product of @a and @b
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div     - Returns the result of dividing @a by @b
 *
 * @a:          To be divided by @b
 * @b:          Used to divide @a
 *
 * Return:      The result of dividing @a by @b
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod     - Returns the remainder of dividing @a by @b
 *
 * @a:          To be divided by @b
 * @b:          Used to divide @a
 *
 * Return:      The remainder of dividing @a by @b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
